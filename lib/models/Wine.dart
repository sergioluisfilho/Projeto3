class Wine {
  String id;
  String country;
  String description;
  String points;
  String price;
  String title;
  String variety;

  Wine(id, country, description, points, price, title, variety) {
    this.id = id;
    this.country = country;
    this.description = description;
    this.points = points;
    this.price = price;
    this.title = title;
    this.variety = variety;
  }
}
